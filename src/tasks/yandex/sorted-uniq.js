/**
 * Удалить дубликаты из отсортированного потока чисел
 *
 * Задача: Прочитать n чисел из потока ввода и вывести только уникальные значения
 * (без повторений подряд идущих одинаковых чисел).
 *
 * Подход:
 * Читаем числа по одному и сравниваем с предыдущим. Если число отличается от предыдущего,
 * выводим его. Это работает только для отсортированного массива.
 *
 * Паттерны: #arrays, #two_pointers, #strings
 * Сложность: O(n)
 */
const readline = require("readline");

const rl = readline.createInterface({
  input: process.stdin,
  output: process.stdout,
});

let n; // Количество чисел для чтения
let count = 0; // Счётчик прочитанных чисел
let prev = null; // Предыдущее прочитанное число

rl.on("line", (line) => {
  if (count === 0) {
    // Первая строка - количество чисел
    n = parseInt(line, 10);
  } else {
    const num = parseInt(line, 10);

    // Выводим число только если оно отличается от предыдущего
    // (или если это первое число)
    if (prev === null || num !== prev) {
      console.log(num);
      prev = num;
    }
  }

  count++;

  // Закрываем интерфейс после прочтения всех чисел
  if (count === n + 1) {
    rl.close();
  }
});
