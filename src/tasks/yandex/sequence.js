/**
 * Найти максимальную длину последовательности единиц
 *
 * Задача: Найти максимальную длину последовательности подряд идущих единиц в массиве.
 *
 * Подход:
 * Проходим по массиву один раз, отслеживая текущую длину последовательности единиц.
 * При встрече единицы увеличиваем счётчик, при встрече другого числа сбрасываем.
 * На каждом шаге обновляем максимальную найденную длину.
 *
 * Паттерны: #arrays, #naive
 * Сложность: O(n)
 *
 * @param {number[]} arr - Массив чисел (0 и 1)
 * @returns {number} Максимальная длина последовательности единиц
 */
function seq(arr) {
  let maxLen = 0; // Максимальная найденная длина последовательности
  let currLen = 0; // Текущая длина последовательности единиц

  for (let i of arr) {
    if (i === 1) {
      // Увеличиваем счётчик текущей последовательности
      currLen++;
    } else {
      // Встретили не единицу - сбрасываем счётчик
      currLen = 0;
    }

    // Обновляем максимальную длину
    maxLen = Math.max(maxLen, currLen);
  }

  return maxLen;
}

console.log(seq([1, 0, 1, 0, 1]));
